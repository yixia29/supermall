<template>
  <div class="home-swiper">
    <swiper>
    <swiper-item v-for="(item,key) in banners" :key="key">
      <a :href="item.link">
        <img :src="item.image" alt="" @load="swiperImgLoad">
      </a>
    </swiper-item>
  </swiper>
  </div>
</template>

<script>
import { Swiper,SwiperItem} from '@/components/common/swiper';
export default {
    props:{
        banners:{
            type:Array,
            default(){
                return []
            }
        }
    },
    data(){
      return {
        isLoad:false
      }
    },
components:{
    Swiper,
    SwiperItem
},
methods:{
  swiperImgLoad(){
    if(!this.isLoad){
    this.$emit('swiperImgLoad')
    this.isLoad=true
    }
  }
}
}
</script>

